#include "../../libft.h"

void	*ft_memset(void	*s, int c, size_t n)
{
	void	*ptr;

	ptr = s;
	while (n > 0)
	{
		*(unsigned char *)s = c;
		s++;
		n--;
	}
	return (ptr);
}
